public class BankAccount {
	private int accountNumber;
	private String accountTitle;
	
	void setterAccountNumber(int AN) {
		accountNumber = AN;
	}
	int getterAccountNumber() {
		return accountNumber;
	}
	
	void setterAccountTitle(String AT) {
		accountTitle = AT;
	}
	String getterAccountTitle() {
		return accountTitle;
	}
}
